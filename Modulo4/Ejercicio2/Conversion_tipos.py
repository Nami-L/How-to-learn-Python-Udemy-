# Ejemplo de concatenación o suma de valores
numero1_cadena = '10'
print(f'Numero 1 en cadea : {numero1_cadena}')
numero2_cadena = '20'
print(f'Numero 2 en cadena : {numero2_cadena} ')

resultado = numero1_cadena + numero2_cadena # el + une dos cacdenas como una concatenación
print(f'Concatenación de cadenas : {resultado}')
# Convertir las cadenas a enteros

numero1_cadena = int(numero1_cadena)
numero2_cadena = int(numero2_cadena)
resultado_entero = numero1_cadena + numero2_cadena  
print(f'La suma de los numeros es {resultado_entero}')

