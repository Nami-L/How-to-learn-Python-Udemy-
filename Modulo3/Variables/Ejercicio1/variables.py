# Codigo para declarar variables en Python

edad = 28
altura = 1.75
pais = "Mexico"

# Acceder a las variables
print ('Valores iniciales:')
print ('Edad:', edad)
print ('Altura:', altura)
print ('Pais:', pais)

# Modificar una variable

edad = 29
altura = 1.76
print ('Valores modificados:')
print ('Edad:', edad)
print ('Altura:', altura)
print ('Pais:', pais)
