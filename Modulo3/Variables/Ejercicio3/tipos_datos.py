# Ejemplo de tipos de datos en Python

# Entero
edad = 30
print("Edad (entero):", edad)

# Flotante
altura = 1.75
print("Altura (flotante):", altura)

# Cadena de texto
nombre = "Juan"
print("Nombre (cadena):", nombre)

# Booleano
es_estudiante = True
print("Es estudiante (booleano):", es_estudiante)
    