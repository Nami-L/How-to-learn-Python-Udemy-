# Operadores de Asingacion Compuestos

a,b = 10,5

print(f'El valor de a es : {a}, y el valor de b es : {b}')


#operador conmpuestao de suma es Â´+=
a += b # a = a + b
print(f'El valor de a despues de la suma es : {a}')

a -= b # a = a -b
print(f'El valor de a despues de la resta es : {a}')

a *= b # a = a * b
print(f'El valor de a despues de la multiplicacion es : {a}')

a /= b # a = a / b
print(f'El valor de a despues de la division es : {a}')

a //= b # a = a // b
print(f'El valor de a despues de la division entera es : {a}')

a **= b # a = a ** b
print(f'El valor de a despues de la potencia es : {a}')

a %= b # a = a % b
print(f'El valor de a despues del modulo es : {a}')

