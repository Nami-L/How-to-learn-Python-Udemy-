# Operadores de logicos

print('*** Operadores lógico and ***')

condicion1 = False
condicion2= False

resultado = condicion1 and condicion2
print(f'El resultado de la operacion logica and entre {condicion1} y {condicion2} es : {resultado}')


print('*** Operadores lógico or ***')
print(f'El resultado de la operacion logica or entre {condicion1} y {condicion2} es : {resultado}')


print('*** Operadores lógico negado ***')
resultado = not condicion1
print(f'El resultado de la operacion logica negado de {condicion1} es : {resultado}')