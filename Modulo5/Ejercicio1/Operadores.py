# Operadores Aritmeticos

a = 10
b= 3

# suma 
suma = a + b
print(f'La suma es : {suma}')

#resta 
resta = a - b
print(f'La resta es: {resta}')

#multiplicacion
multiplicacion = a * b
print(f'La multiplicacion es: {multiplicacion}')

#Division
division = a / b
print(f'La division es: {division:.2f}')

#Division entera
division_entera = a // b
print(f'La division entera es: {division_entera}')

#Potencia
potencia = a ** b
print(f'La potencia es: {potencia}')

#Modulo, es decir el residuo de la division
modulo = a % b
print(f'El modulo es: {modulo}')


